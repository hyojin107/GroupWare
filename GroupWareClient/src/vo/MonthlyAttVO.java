package vo;

public class MonthlyAttVO {
	private int month;
	private int work;
	private int outMiss;
	private int late;
	private int workTime;
	private int jan;
	
	public int getMonth() {
		return month;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	public int getWork() {
		return work;
	}
	public void setWork(int work) {
		this.work = work;
	}
	public int getOutMiss() {
		return outMiss;
	}
	public void setOutMiss(int outMiss) {
		this.outMiss = outMiss;
	}
	public int getLate() {
		return late;
	}
	public void setLate(int late) {
		this.late = late;
	}
	public int getWorkTime() {
		return workTime;
	}
	public void setWorkTime(int workTime) {
		this.workTime = workTime;
	}
	public int getJan() {
		return jan;
	}
	public void setJan(int jan) {
		this.jan = jan;
	}
}
